package caracteristicas_linguagem.modificadores_acesso;

public class OutrosModificadores {

    // abstract
    // static
    // final

    /*
    Abstract:
    Esse modificador não é aplicado nas variáveis, apenas em classes e
    métodos. Uma classe abstrata não pode ser instanciada. Se houver alguma declaração
    de um método como abstract, a classe também deve ser marcada como abstract.
     */


    /*
    Static:
    É usado para a criação de uma variavél que poderá ser acessada por
    todas as instâncias de objetos desta classe como uma variável comum,
    ou seja, a variável criada será a mesma em todas as instâncias e quando seu conteúdo
    é modificado numa das instâncias, a modificação ocorre em todas as demais. E nas
    declarações de métodos ajudam no acesso direto à classe, portanto não é necessário
    instanciar um objeto para acessar o método.
     */


    /*
    Final:
    Quando é aplicado na classe, não permite estender, nos métodos impede que o mesmo
    seja sobrescrito (@override) na subclasse, e nos valores de variáveis não pode ser
    alterado depois que já tenha sido atribuído um valor
     */

    /*
    Ver exemplos
     */
}
